f=@(x) ((12*x)*(x^(1/2))) - ((8+12*pi)*x) + ((24*x^(1/2) - 16)*sin(x^(1/2))) + ((3*pi^2 + 8*pi - 24)*x^(1/2)) - (2*pi^2) + 16;
df=@(x) (18*x^(1/2)) + ((-24 + 8*pi + 3*pi^2)/(2*x^(1/2))) + ((12*sin(x^(1/2)))/(x^(1/2))) + (((24*x^(1/2) - 16)*cos(x^(1/2)))/(2*x^(1/2))) - (12*pi) - 8;
x(1) = 0.3;
steps = 30;
for i=1:steps
  x(i+1) = x(i) - (f(x(i))/df(x(i)));
end
r = x(steps+1);
e = x-r;
for i=1:steps
  rat(i+1) = e(i+1)/e(i)^2;
end
rat(1)=0;
